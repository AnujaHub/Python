a = 1 #a is an integer
b = 2.5 #b is a float   
c = "Hello" #c is a string
d = True #d is a boolean
e = [1, 2, 3] #e is a list
f = (4, 5, 6) #f is a tuple
g = { 'name': 'Alice', 'age': 30 } #g is a dictionary
h = {7, 8, 9} #h is a set
i = None #i is a NoneType   

# flase and none(nothing) are different
print(type(a))  # <class 'int'>
print(type(b))  # <class 'float'>
print(type(c))  # <class 'str'>
print(type(d))  # <class 'bool'>
print(type(e))  # <class 'list'>
print(type(i))  # <class 'NoneType'>
print(type(f))  # <class 'tuple'>
print(type(g))  # <class 'dict'>
print(type(h))  # <class 'set'> 


